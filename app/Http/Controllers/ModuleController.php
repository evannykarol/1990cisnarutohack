    public function querymoduls($id)
    {
        $Modul = Moduls::where('id_moduls_group','=',$id)->get();
        $data = [];
        foreach ($Modul as $Moduls) {
            $data[] = [
                        "id"            => $Moduls->id,
                        "name"          => $Moduls->name,
                        "icon"          => '<i class="'.$Moduls->icon.'"></i>',
                        "is_active"     => $Moduls->is_active
                      ];
        }
        return response()->json($data);
    }

